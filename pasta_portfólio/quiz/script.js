let currentQuiz = [];
let currentQuestion = 0;
let score = 0;

const menuEl = document.getElementById("menu");
const questionContainer = document.getElementById("question-container");
const questionEl = document.getElementById("question");
const optionsEl = document.getElementById("options");
const feedbackEl = document.getElementById("feedback");
const nextBtn = document.getElementById("next-btn");
const resultContainer = document.getElementById("result-container");
const scoreEl = document.getElementById("score");
const scorePercentageEl = document.getElementById("score-percentage");
const progressEl = document.getElementById("progress");

function startQuiz(topic) {
  currentQuiz = quizzes[topic];
  currentQuestion = 0;
  score = 0;
  menuEl.style.display = "none";
  resultContainer.style.display = "none";
  questionContainer.style.display = "block";
  updateProgress();
  loadQuestion();
}

function loadQuestion() {
  const q = currentQuiz[currentQuestion];
  questionEl.textContent = q.question;
  optionsEl.innerHTML = "";
  feedbackEl.textContent = "";
  feedbackEl.className = "feedback";
  nextBtn.style.display = "none";

  q.options.forEach((option, index) => {
    const button = document.createElement("button");
    button.textContent = option;
    button.addEventListener("click", () => selectAnswer(index));
    optionsEl.appendChild(button);
  });
}

function selectAnswer(index) {
  const q = currentQuiz[currentQuestion];
  const buttons = optionsEl.querySelectorAll("button");
  
  // Desabilita todos os botões
  buttons.forEach(btn => btn.disabled = true);
  
  // Marca a resposta correta e a selecionada
  buttons[q.answer].classList.add("correct");
  
  if (index !== q.answer) {
    buttons[index].classList.add("incorrect");
    feedbackEl.textContent = "❌ Errado! " + q.feedback;
    feedbackEl.className = "feedback incorrect";
  } else {
    score++;
    feedbackEl.textContent = "✔️ Correto! " + q.feedback;
    feedbackEl.className = "feedback correct";
  }
  
  nextBtn.style.display = "block";
}

nextBtn.addEventListener("click", () => {
  currentQuestion++;
  if (currentQuestion < currentQuiz.length) {
    updateProgress();
    loadQuestion();
  } else {
    showResult();
  }
});

function updateProgress() {
  const progress = ((currentQuestion) / currentQuiz.length) * 100;
  progressEl.style.width = `${progress}%`;
}

function showResult() {
  questionContainer.style.display = "none";
  resultContainer.style.display = "block";
  const percentage = Math.round((score / currentQuiz.length) * 100);
  
  scoreEl.textContent = `Você acertou ${score} de ${currentQuiz.length} perguntas.`;
  scorePercentageEl.textContent = `${percentage}%`;
  
  // Adiciona uma mensagem personalizada baseada na pontuação
  let message = "";
  if (percentage >= 90) message = "Excelente! Você é um expert no assunto!";
  else if (percentage >= 70) message = "Muito bom! Você conhece bem o tema.";
  else if (percentage >= 50) message = "Bom trabalho! Continue aprendendo.";
  else message = "Não desanime! Tente novamente para melhorar.";
  
  scoreEl.textContent += ` ${message}`;
}

function showMenu() {
  menuEl.style.display = "block";
  resultContainer.style.display = "none";
  progressEl.style.width = "0%";
}
